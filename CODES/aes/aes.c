/* AES Operations */
static inline uint8_t gal_mul(uint8_t a, uint8_t b) {
    uint8_t p = 0;
    for (int i = 0; i < 8; i++) {
        if (b & 1) p ^= a;
        uint8_t hi = a & 0x80;
        a <<= 1;
        if (hi) a ^= 0x1b;
        b >>= 1;
    }
    return p;
}

static void aes_encrypt_core(uint8_t s[4][4]) {
    // Initial Round
    for (int c = 0; c < 4; c++)
        for (int r = 0; r < 4; r++) s[r][c] ^= round_keys[0][c * 4 + r];

    // Rounds 1 to 13
    for (int rnd = 1; rnd < 14; rnd++) {
        // SubBytes
        for (int r = 0; r < 4; r++)
            for (int c = 0; c < 4; c++) s[r][c] = sbox[s[r][c]];
        // ShiftRows
        uint8_t t;
        t = s[1][0]; s[1][0] = s[1][1]; s[1][1] = s[1][2]; s[1][2] = s[1][3]; s[1][3] = t;
        t = s[2][0]; s[2][0] = s[2][2]; s[2][2] = t; 
        t = s[2][1]; s[2][1] = s[2][3]; s[2][3] = t;
        t = s[3][3]; s[3][3] = s[3][2]; s[3][2] = s[3][1]; s[3][1] = s[3][0]; s[3][0] = t;
        // MixColumns
        for (int c = 0; c < 4; c++) {
            uint8_t a[4];
            for (int r = 0; r < 4; r++) a[r] = s[r][c];
            s[0][c] = gal_mul(a[0],2) ^ gal_mul(a[1],3) ^ a[2] ^ a[3];
            s[1][c] = a[0] ^ gal_mul(a[1],2) ^ gal_mul(a[2],3) ^ a[3];
            s[2][c] = a[0] ^ a[1] ^ gal_mul(a[2],2) ^ gal_mul(a[3],3);
            s[3][c] = gal_mul(a[0],3) ^ a[1] ^ a[2] ^ gal_mul(a[3],2);
        }
        // AddRoundKey
        for (int c = 0; c < 4; c++)
            for (int r = 0; r < 4; r++) s[r][c] ^= round_keys[rnd][c * 4 + r];
    }

    // Final Round
    for (int r = 0; r < 4; r++)
        for (int c = 0; c < 4; c++) s[r][c] = sbox[s[r][c]];
    uint8_t t;
    t = s[1][0]; s[1][0] = s[1][1]; s[1][1] = s[1][2]; s[1][2] = s[1][3]; s[1][3] = t;
    t = s[2][0]; s[2][0] = s[2][2]; s[2][2] = t; 
    t = s[2][1]; s[2][1] = s[2][3]; s[2][3] = t;
    t = s[3][3]; s[3][3] = s[3][2]; s[3][2] = s[3][1]; s[3][1] = s[3][0]; s[3][0] = t;
    for (int c = 0; c < 4; c++)
        for (int r = 0; r < 4; r++) s[r][c] ^= round_keys[14][c * 4 + r];
}

int main(void) {
    // 1. Initial State
    uint8_t state[4][4] = {
        {0x00, 0x44, 0x88, 0xcc}, // Row 0
        {0x11, 0x55, 0x99, 0xdd}, // Row 1
        {0x22, 0x66, 0xaa, 0xee}, // Row 2
        {0x33, 0x77, 0xbb, 0xff}  // Row 3
    };

    // 2. Encrypt
    aes_encrypt_core(state);

    // 3. Forced Memory Writes to Absolute Address 0x0
    // We use volatile to ensure these stores appear on the bus in Vivado
    volatile uint32_t *base_mem = (volatile uint32_t *)0x00000000;

    // Pack each column into a 32-bit word and write to mem[0], [1], [2], [3]
    base_mem[0] = ((uint32_t)state[0][0] << 24) | ((uint32_t)state[1][0] << 16) | 
                  ((uint32_t)state[2][0] << 8)  | ((uint32_t)state[3][0]);

    base_mem[1] = ((uint32_t)state[0][1] << 24) | ((uint32_t)state[1][1] << 16) | 
                  ((uint32_t)state[2][1] << 8)  | ((uint32_t)state[3][1]);

    base_mem[2] = ((uint32_t)state[0][2] << 24) | ((uint32_t)state[1][2] << 16) | 
                  ((uint32_t)state[2][2] << 8)  | ((uint32_t)state[3][2]);

    base_mem[3] = ((uint32_t)state[0][3] << 24) | ((uint32_t)state[1][3] << 16) | 
                  ((uint32_t)state[2][3] << 8)  | ((uint32_t)state[3][3]);

    // 4. Trap core to prevent it from executing uninitialized memory
    while(1) {
        __asm__ volatile ("nop");
    }

    return 0;
}